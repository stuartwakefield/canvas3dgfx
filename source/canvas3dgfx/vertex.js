/**
 * Represents the vertex of a model in 3D space.
 **/
class Vertex {

  /**
   * @param {Number} x the position along the x axis
   * @param {Number} y the position along the y axis
   * @param {Number} z the position along the z axis
   **/
  constructor(x, y, z) {
    this.x = x
    this.y = y
    this.z = z
  }

}
